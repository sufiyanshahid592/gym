        <h2>Fees as low as …% *</h2>
        <small>* For qualified payments</small>
        <p>You’ve probably seen this before when evaluating processing costs – a low processing fee with phrasing such as “As low as…” or “starting at …”, ending with an asterisk.</p> 
        <p>Different transactions are charged different rates by the credit-card companies, depending on the type of card used and the potential risk of the transaction.</p>
        <p>Payment gateways then roll those rates on to you with their own pricing scheme, often separating payments into classes of <b>&quot;qualified&quot;</b>, <b>&quot;mid-qualified&quot;</b> and <b>&quot;non-qualified&quot;</b> payments. Qualified payments are charged the advertised “starting at…” rates, while non-qualified payments are charged increasingly higher rates depending on different factors (also known as three-tier pricing).</p>
        <p>Recurring payments that are charged using a card on file automatically are charged the highest fees. For a membership based business, that means most of the transactions will be <b>considered non-qualified and be subject to much higher fees</b>.</p>
        <p>Other charges can include setup fees, monthly fees, charge-back fees and more.</p>
        <p>At <b>Gymdesk</b> we partnered with payment processor who offer simplified rates with that are optimized to online and recurring billing, and no monthly fees. Let's see how it compares to <b>Mindbody's</b> fees:</p>

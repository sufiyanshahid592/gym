            <h2>Frequently Asked Questions</h2>
            <ul>
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">How do you keep track of attendance?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                        <div itemprop="text">
                        <p>You can track attendance using a laptop, tablet or even a smartphone, using the browser. A member checks-in using a numeric code or via name search. Gymdesk will pull up the member profile and show which classes they can check-in and how many sessions they have remaining.</p>
                        </div>
                    </div>
                </li>
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">What is an online attendance system?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                    <p itemprop="text">An online attendance system allows you to record attendance through a web app, remotely, without the need for downloading software on a local computer or using analog records. More than that, it keeps track of attendance data so that it can be presented in ways that help gym or school administrators understand better how members are using their services.</p>
                    </div>
                </li>
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">Can you track attendance using a barcode scanner?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                    <p itemprop="text">Gymdesk integrates with any USB barcode scanner to allow easy scanning and attendance tracking. You can generate attractive membership cards with barcodes from a member’s profile and print them to PVC or any cardstock compatible with your printer.</p>
                    </div>
                </li>
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">Can you track past attendance?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                    <p itemprop="text">Yes, Gymdesk allows for you to backdate attendance from the attendance dashboard in your manager account. Simply find the name of the member, hit the log attendance button, and then adjust the date.</p>
                    </div>
                </li>
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">Can you input attendance in bulk?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                    <p itemprop="text">Yes, one of the power features of Gymdesk allows you to check in a large number of members all at once. It can be found from the attendance section on the “Multiple Check-In” tab. If you track attendance by taking pictures or writing down names manually, you can more easily enter them into the system with this feature.</p>
                    </div>

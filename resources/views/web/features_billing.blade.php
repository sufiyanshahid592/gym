            <h2>Frequently Asked Questions</h2>
            <ul>
                
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">What is a merchant account?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                    <p itemprop="text">A merchant account is a type of bank account that allows you to accept payments - mostly debit or credit card payments. Previously, you would need to open a merchant account if you want to process payment cards for your business, and a separate payment gateway to accept those payments online or via a card reader. Nowadays, all-in-one payment providers such as Stripe or Square allow you to process payments without opening multiple types of accounts.</p>
                    </div>
                </li>
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">How do I start with payment processing?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                    <p itemprop="text">You would normally need to open a merchant account and add a payment gateway, both of which have their own process and associated fees. Newer payment providers such as Stripe and Square allow you to create one account with a simplified fee structure. Gymdesk integrates with Stripe, Square and Authorize.net for payments, allowing you get start accepting payments quickly, with simple and predictable costs.</p>
                    </div>
                </li>
                <li itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
                    <h3 itemprop="name">What are the payment processing fees?</h3>
                    <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                    <p itemprop="text">Payment processing fees vary by payment provider and your country. In the U.S, most payment processors cost 2.9% + 30c per transaction. Our Authorize.net partner, Friendly Payments, can negotiate that down to 2.75% + 25c per transaction. Gymdesk does not charge any additional fees for payment processing on top of the base fees. Check the Stripe and Square websites for pricing in countries other than the US.</p>
                    </div>
                </li>
                
                
